// thao tác remove nội dung
function removeHtml(address){
    document.querySelector(`${address}`).innerHTML = "";
}

// hiệu ứng load
function loadAnimation(){
    let loadElement = document.createElement('div');
    loadElement.classList.add('load');
    document.querySelector('.content').appendChild(loadElement);
}

function removeLoadAnimation(){
    var x = document.querySelector('.load');
    if(x){
        x.remove();
    }
}

function scrollY(address){
    document.querySelector(`${address}`).scrollIntoView({
        behavior: 'smooth',   // mượt
        block: 'start',       // căn phần tử ở đầu viewport
        inline: 'nearest'     // giữ nguyên theo chiều ngang
    });
}

