function render(address,data){  
    data.forEach(function(item){
        if(address.includes('tag')){
            newaddress = address.slice(3);
            var addPlace = document.getElementsByTagName(`${newaddress}`);
            addPlace = addPlace[addPlace.length-1]; 
        }
        else{
            var addPlace = document.querySelectorAll(`${address}`);
            addPlace = addPlace[addPlace.length-1]; 
        }
        
        var htmls = document.createElement(`${item.tag}`);
        if(item.content){
            htmls.innerText = item.content;
        }
        if(item.id){
            htmls.id = item.id;
        }
        if(item.class){
            htmls.classList.add(...(item.class.split(" ")));
        }
        if(item.src){
            htmls.src = item.src;
        }
        if(item.href){
            htmls.href = item.href;
        }
        if(item.title){
            htmls.title = item.title
        }
        if(item.value){
            htmls.value = item.value;
        }
        addPlace.appendChild(htmls);
        if(item.children){
            if(!item.class){
                render('tag'+`${item.tag}`,item.children);
            }
            else render('.'+item.class.replace(' ','.'),item.children);
        }
    }); 
}

